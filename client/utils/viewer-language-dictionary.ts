export const translateViewerText = (value: string, language: ViewerLanguageCode) => {
  if (!value.trim()) return value;

  const leading = value.match(/^\s*/)?.[0] || "";
  const trailing = value.match(/\s*$/)?.[0] || "";
  const body = value.slice(leading.length, value.length - trailing.length);
  const normalized = body.replace(/\s+/g, " ").trim();

  const exact = exactTranslations[language][normalized];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const [pattern, render] of regexTranslations[language]) {
    const match = normalized.match(pattern);
    if (match) return `${leading}${render(match)}${trailing}`;
  }

  let translated = body;
  for (const [source, target] of phraseTranslations[language]) {
    translated = translated.split(source).join(target);
  }

  return `${leading}${translated}${trailing}`;
};
